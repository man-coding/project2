package main;

/*
 * 실수형 연습
 * */
public class Ex06 {

	public static void main(String[] args) {
		
		float f = 3.14f; //float형은 식별자를 붙어야한다
		double d = 3.14111;
		
		//float형 변수에 값을 저장할 때 식별자가 없으면 에러가 발생한다
		//f = 3.0; <- 에러
		
		System.out.println(f);
		System.out.println(d);
	}

}

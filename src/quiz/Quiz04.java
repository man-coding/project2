package quiz;

/*
 * <자료형>
 * 변수 a를 만들고 0을 대입하세요
 * 변수 b를 만들고 -1000을 대입하세요
 * 변수 c를 만들고 1.15를 대입하세요
 * 변수 d를 만들고 a를 대입하세요
 * 변수 e를 만들고 2023년은 토끼띠 입니다를 대입하세요
 * */

public class Quiz04 {

	public static void main(String[] args) {
		
		byte a = 0; // byte, short, int, long
		int b = -1000; // short, int, long
		float c = 1.15f; // float, double
		char d = 'a'; // char
		String e = "2023년은 토끼띠 입니다"; //String
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
	}

}
